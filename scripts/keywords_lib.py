def extract_keywords(title: str, intro: str, is_male: bool = False) -> List[str]:
    """
    从书名和简介中提取关键词标签（基于关键词词库匹配）。

    Args:
        title: 书名
        intro: 简介
        is_male: 是否男频（决定关键词库）

    Returns:
        关键词列表，如 ["重生", "系统", "甜宠", "都市"]
    """
    if not title and not intro:
        return []

    text = f"{title or ''} {intro or ''}".lower()
    found = []

    # 通用关键词库
    for keyword, patterns in COMMON_KEYWORDS.items():
        if keyword in found:
            continue
        for pattern in patterns:
            if pattern in text:
                found.append(keyword)
                break

    # 性别专属关键词库
    gender_kw = MALE_KEYWORDS if is_male else FEMALE_KEYWORDS
    for keyword, patterns in gender_kw.items():
        if keyword in found:
            continue
        for pattern in patterns:
            if pattern in text:
                found.append(keyword)
                break

    return found[:6]  # 最多 6 个标签
